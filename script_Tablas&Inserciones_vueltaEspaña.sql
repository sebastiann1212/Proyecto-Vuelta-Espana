CREATE TABLE Reglas (
	idRegla INTEGER PRIMARY KEY NOT NULL,
	nombre VARCHAR(100) NOT NULL,
	fechaVigencia DATE NOT NULL,
	fechaCaducidad DATE 
);

CREATE TABLE TipoEtapas (
	idTipoEtapa INTEGER PRIMARY KEY NOT NULL,
	nombre VARCHAR(100) NOT NULL,
	descripcion VARCHAR(500)
);

CREATE TABLE TipoDePuntos (
	idTipoPunto INTEGER PRIMARY KEY NOT NULL,
	nombre VARCHAR(100) NOT NULL,
	descripcion VARCHAR(500) NOT NULL
);

CREATE TABLE Paises (
	idPais INTEGER PRIMARY KEY NOT NULL,
	nombre VARCHAR(100) NOT NULL
);

CREATE TABLE Ediciones (
	idEdicion INTEGER PRIMARY KEY NOT NULL, 
	añoRealizacion INTEGER NOT NULL
);

CREATE TABLE TipoPatrocinadores(
	idTipoPatrocinador INTEGER PRIMARY KEY NOT NULL,
	nombre VARCHAR(500) NOT NULL
);

CREATE TABLE TipoDeRoles (
	idTipoRol INTEGER PRIMARY KEY NOT NULL,
	nombre VARCHAR(500) NOT NULL,
	descripcion VARCHAR(500)
);

CREATE TABLE Ciudades (
	idCiudad INTEGER PRIMARY KEY NOT NULL,
	nombre VARCHAR(100) NOT NULL
);

CREATE TABLE TecnicasDePedaleo (
	idTecnicaPedaleo INTEGER PRIMARY KEY NOT NULL,
	nombre VARCHAR(100) NOT NULL,
	descripcion VARCHAR(1000) NOT NULL
);

CREATE TABLE TipoDeMaillot (
	idTipoMaillot INTEGER PRIMARY KEY NOT NULL,
	nombre VARCHAR(200) NOT NULL, 
	descripcion VARCHAR(500)
);

-----------------------------------------------------------------
CREATE TABLE Patrocinadores (
	idPatrocinador INTEGER PRIMARY KEY NOT NULL,
	nombre VARCHAR(500) NOT NULL,
	idTipoPatrocinador INTEGER NOT NULL,
	FOREIGN KEY (idTipoPatrocinador) REFERENCES TipoPatrocinadores(idTipoPatrocinador)
);

CREATE TABLE Etapas (
	idEtapa INTEGER PRIMARY KEY NOT NULL,
	numeroEtapa INTEGER NOT NULL,
	distanciaRecorrida DECIMAL(10, 2) NOT NULL,
	fechaEtapa DATE NOT NULL,
	idTipoEtapa INTEGER NOT NULL,
	idCiudadDeInicio INTEGER NOT NULL,
	idCiudadDeFin INTEGER NOT NULL,
	idEdicion INTEGER NOT NULL,
	FOREIGN KEY(idTipoEtapa) REFERENCES TipoEtapas(idTipoEtapa),
	FOREIGN KEY(idCiudadDeInicio) REFERENCES Ciudades(idCiudad),
	FOREIGN KEY(idCiudadDeFin) REFERENCES Ciudades(idCiudad),	
	FOREIGN KEY (idEdicion) REFERENCES Ediciones(idEdicion)
);

CREATE TABLE Ciclistas (
	idCiclista SERIAL PRIMARY KEY NOT NULL,
	primerNombre VARCHAR(300) NOT NULL,
	segundoNombre VARCHAR(300),
	primerApellido VARCHAR(300) NOT NULL,
	segundoApellido VARCHAR(300),
	fechaNacimiento DATE NOT NULL,
	idPaisNacimiento INTEGER NOT NULL,
	FOREIGN KEY(idPaisNacimiento) REFERENCES Paises(idPais)
);

CREATE TABLE RegistroDePuntos (
	idEtapa INTEGER NOT NULL,
	idCiclista INTEGER NOT NULL,
	idTipoPunto INTEGER NOT NULL,
	cantidadPuntosObtenidos INTEGER NOT NULL,
	PRIMARY KEY(idEtapa, idCiclista, idTipoPunto),
	FOREIGN KEY (idEtapa) REFERENCES Etapas(idEtapa),
	FOREIGN KEY(idCiclista) REFERENCES Ciclistas(idCiclista),
	FOREIGN KEY(idTipoPunto) REFERENCES TipoDePuntos(idTipoPunto)
);


CREATE TABLE PatrocinadoresEquipos() INHERITS (Patrocinadores);

CREATE TABLE PatrocinadoresEventos()INHERITS (Patrocinadores);

ALTER TABLE PatrocinadoresEquipos ADD PRIMARY KEY (idPatrocinador);
ALTER TABLE PatrocinadoresEventos ADD PRIMARY KEY (idPatrocinador);

CREATE TABLE RegistroDeRoles (
	idCiclista INTEGER NOT NULL,
	idTipoRol INTEGER NOT NULL,
	idEdicion INTEGER NOT NULL,
	PRIMARY KEY(idCiclista, idTipoRol, idEdicion),
	FOREIGN KEY(idEdicion) REFERENCES Ediciones(idEdicion),
	FOREIGN KEY(idCiclista) REFERENCES Ciclistas(idCiclista),
	FOREIGN KEY(idTipoRol) REFERENCES TipoDeRoles(idTipoRol)
);

CREATE TABLE RegistroEtapas (
	idEtapa INTEGER NOT NULL,
	idCiclista INTEGER NOT NULL,
	duracion TIME NOT NULL,
	bonificacion TIME,
	penalizacion TIME,
	PRIMARY KEY(idEtapa, idCiclista),
	FOREIGN KEY(idEtapa) REFERENCES Etapas(idEtapa),
	FOREIGN KEY(idCiclista) REFERENCES Ciclistas(idCiclista)
);



CREATE TABLE EdicionesDeCiclistas (
	idEdicion INTEGER NOT NULL,
	idCiclista INTEGER NOT NULL,
	PRIMARY KEY(idEdicion, idCiclista),
	FOREIGN KEY(idEdicion) REFERENCES Ediciones(idEdicion),
	FOREIGN KEY(idCiclista) REFERENCES Ciclistas(idCiclista)
);


CREATE TABLE Abandonos (
	idAbandono INTEGER PRIMARY KEY NOT NULL,
	idCiclista INTEGER NOT NULL,
	razonAbandono VARCHAR(500) NOT NULL,
	idEtapa INTEGER NOT NULL,
	FOREIGN KEY(idEtapa) REFERENCES Etapas(idEtapa),
	FOREIGN KEY(idCiclista) REFERENCES Ciclistas(idCiclista)
);

CREATE TABLE Sanciones (
	idSancion INTEGER PRIMARY KEY NOT NULL,
	idCiclista INTEGER NOT NULL,
	idEtapa INTEGER NOT NULL,
	idRegla INTEGER NOT NULL,
	FOREIGN KEY(idEtapa) REFERENCES Etapas(idEtapa),
	FOREIGN KEY(idRegla) REFERENCES Reglas(idRegla),
	FOREIGN KEY(idCiclista) REFERENCES Ciclistas(idCiclista)
);

CREATE TABLE Maillot (
	idMaillot INTEGER PRIMARY KEY NOT NULL,
	nombre VARCHAR(200) NOT NULL,
	idTipoMaillot INTEGER NOT NULL,
	idPatrocinadorMaillot INTEGER NOT NULL,
	FOREIGN KEY(idTipoMaillot) REFERENCES TipoDeMaillot(idTipoMaillot),
    FOREIGN KEY (idPatrocinadorMaillot) REFERENCES PatrocinadoresEventos(idPatrocinador)
);

CREATE TABLE Equipos (
	idEquipo INTEGER PRIMARY KEY NOT NULL,
	nombre VARCHAR(100) NOT NULL,
	idPais INTEGER NOT NULL,
	FOREIGN KEY(idPais) REFERENCES Paises(idPais)
);

CREATE TABLE EdicionesDeEquipos (
	idEdicion INTEGER NOT NULL,
	idEquipo INTEGER NOT NULL,
	PRIMARY KEY(idEdicion, idEquipo),
	FOREIGN KEY(idEdicion) REFERENCES Ediciones(idEdicion),
	FOREIGN KEY(idEquipo) REFERENCES Equipos(idEquipo)
);

CREATE TABLE RegistroPatrocinadoresEquipos (
	idEquipo INTEGER NOT NULL,
	idPatrocinadorEquipo INTEGER NOT NULL,
	PRIMARY KEY(idPatrocinadorEquipo, idEquipo),
	FOREIGN KEY(idPatrocinadorEquipo) REFERENCES PatrocinadoresEquipos(idPatrocinador),
	FOREIGN KEY(idEquipo) REFERENCES Equipos(idEquipo)
);

CREATE TABLE EquiposDeCiclistas(
	idEquipo INTEGER NOT NULL,
	idCiclista INTEGER NOT NULL,
	idEdicion INTEGER NOT NULL,
	dorsal INTEGER NOT NULL,
	PRIMARY KEY(idEquipo, idCiclista, idEdicion),
	FOREIGN KEY(idEquipo) REFERENCES Equipos(idEquipo),
	FOREIGN KEY(idEdicion) REFERENCES Ediciones(idEdicion),
	FOREIGN KEY(idCiclista) REFERENCES Ciclistas(idCiclista)
);

CREATE TABLE RegistroTecnicaPedaleo(
	idTecnicaPedaleo INTEGER NOT NULL,
	idCiclista INTEGER NOT NULL,
	idEtapa INTEGER NOT NULL,
	PRIMARY KEY(idTecnicaPedaleo, idCiclista, idEtapa),
	FOREIGN KEY(idTecnicaPedaleo) REFERENCES TecnicasDePedaleo(idTecnicaPedaleo),
	FOREIGN KEY(idCiclista) REFERENCES Ciclistas(idCiclista),
	FOREIGN KEY (idEtapa) REFERENCES Etapas(idEtapa)
);

CREATE TABLE RegistroMaillots(
	idCiclista INTEGER NOT NULL,
	idMaillot INTEGER NOT NULL,
	idEtapa INTEGER NOT NULL,
	PRIMARY KEY(idCiclista, idMaillot, idEtapa),
	FOREIGN KEY(idCiclista) REFERENCES Ciclistas(idCiclista),
	FOREIGN KEY (idEtapa) REFERENCES Etapas(idEtapa),
	FOREIGN KEY (idMaillot) REFERENCES Maillot(idMaillot)
);

